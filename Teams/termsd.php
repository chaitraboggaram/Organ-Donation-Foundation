<br><br><b>Dialysis-</b>A mechanical process designed to remove toxic substances from the blood, including correcting the balance of fluids and chemicals in the body and removing wastes when the kidneys are unable to do so. See hemodialysis and peritoneal dialysis.
<br><br><b>Domino Transplant-</b>Although it does not happen often, a domino transplant occurs when patient A needs lungs, but the best treatment is to give that patient a heart and lung combination. Since patient A's heart was good, it can be transplanted into patient B who needs only a heart.
<br><br><b>Donor Designation-</b>Documentation of an individual’s decision to donate organs, eyes, and/or tissues after death, usually designated on a driver’s license or through a State donor registry.
<br><br><b>Donor Registries-</b>A confidential electronic database in which individuals can enter and store their wish to be an organ and tissue donor. Most registries are for a single state, but a few serve more than one state. Most registries have enrollment capacity through the motor vehicle offices and many also have online registry portals. Because registry information is accessible on a 24/7 basis to authorized procurement personnel, it is the safest and quickest way to determine if a deceased individual wanted to be a donor.
