<br><br><br><br><b>Ligaments-</b>Fibrous bands or sheets that link two or more bones, cartilages, or structures together. Ligaments provide stability during rest and movement and protect against excessive movements such as hyper-extension or hyper-flexion. Ligaments can be transplanted. See Connective Tissue.
<br><br><b>Liver-</b>A large reddish-brown organ that secretes bile and is active in the formation of certain blood proteins and in the metabolism of carbohydrates, fats, and proteins. The liver, like the kidneys, assists in the removal of waste and toxins from the blood stream. The liver can be donated by deceased donors, and a liver lobe (section) can be provided by a living donor to be transplanted. The donor's liver will grow to full size, and the transplanted lobe will too.
<br><br><b>Living Donor-</b>A person who donates an organ or tissue while alive.
<br><br><b>Lungs-</b>The organs that enable breathing to take place, providing life-sustaining oxygen to the body and its organs. Air is inhaled into the lungs and oxygen in the air is exchanged for carbon dioxide which is then exhaled. The exchange happens in the blood as it circulates through the sponge-like lung tissue. The lungs can be donated and transplanted. A lung lobe can be donated by a live donor.
<br><br><b>Lymphocytotoxic crossmatch test-</b>The lymphocytotoxic crossmatch test detects antibodies in the recipient that react with donor HLA antigens prior to transplantation.  Lymphocytotoxic crossmatch tests are used primarily for transplant candidates to assess the suitability of a potential donor.  A positive lymphocytotoxic crossmatch identifies antibodies responsible for hyperacute rejection of kidney grafts and is therefore a clear contraindication to transplantation.
