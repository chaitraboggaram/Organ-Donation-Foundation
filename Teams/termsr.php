<br><br><br><br><b>Recipient-</b>In the context of organ and tissue transplantation, this is the patient receiving the donated organ or tissue.
<br><br><b>Recovery-</b>In the context of organ and tissue transplantation, refers to the process of removing organs and tissues from the donor.
<br><br><b>Rejection (Acute and Chronic)-</b>The body's way of protecting itself against a foreign invader such as infectious germs. The body sees the transplanted organ or tissue as a foreign invader and attempts to destroy it. Acute rejection happens very quickly; chronic rejection is the slow failure of a donated organ to function.
<br><br><b>Required Request-</b>A law passed in 1986 requiring hospitals to have a policy in place requiring all families of suitable donors to be asked to give consent to their loved one's organs and tissues to be used for transplant. This law is expected to increase the number of donated organs and tissues for transplantation by giving more people the opportunity to donate.
<br><br><b>Requester-</b>A healthcare professional who discusses organ donation with surviving family members of a potential donor in order to obtain their consent for donation to occur.
