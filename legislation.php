<br><br>The field of organ and tissue donation and transplantation is one of the most regulated areas of health care today.
<br><br>Both state and federal legislation has been put in place to provide the safest and most equitable system for allocation, distribution, and transplantation of donated organs.
<br><br>The Health Resources and Services Administration (HRSA) is the Federal agency with responsibility for oversight of the transplant system in the United States.	
<br><br>The following federal agencies also play a part in this lifesaving process: Centers for Medicare and Medicaid Services (CMS), Centers for Disease Control and Prevention (CDC), National Institutes of Health (NIH), Agency for Healthcare Research and Quality (AHRQ), and Food and Drug Administration (FDA). All these agencies are part of the U.S. Department of Health and Human Services.
<br><br>State laws facilitate organ and tissue donation commitments and make transplants happen.
