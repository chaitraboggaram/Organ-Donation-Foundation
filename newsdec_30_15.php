<big><big><b>Mumbai Equals Last Year's Record With Its 41st Organ Donation This Year</b></big></big><br><br><br>
<big>With the organ donation rates taking backstage in the city, Mumbai has been able to pull its 41st organ donation this year.
<img src="" border="2" align="left">
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspA 51-year-old homemaker suffered a ischemic stroke and was declared brain dead at the Fortis Hospital in Mumbai. Her husband agreed to donate both her kidneys, corneas and liver. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspDoctors at the hospital quickly transplanted the organs to three patients who were suffering with end stage organ failures. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspSantosh Sorate, transplant coordinator at Fortis Hospital, said, “When we approached the family to explain them about organ donation, we learnt that they were well-aware about it and were willing to donate their relative’s organs. Because of the positive approach and initiative of the family members, we had won half of the battle. After getting their consent, we explained them about the formalities and procedures involved.” 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspWith this donation, Mumbai has managed to make 41 cadaver donations this year, in which 70 kidneys, 36 liver and five hearts have been donated.
