<br><br>Brain death and a coma are not the same thing. It is possible for a person to recover from a coma. It is not possible to recover from brain death.
<br><br>Brain death is most often caused by head trauma, a stroke, a ruptured aneurysm that causes bleeding in the brain, a gunshot wound, or drowning.
