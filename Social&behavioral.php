		<th>Project Title:</th><td>	Using an interactive website, social media, and gamification to engage/reframe deceased organ donation for Asian/Pacific Islander college students</td>
</tr>
</table>
The proportion of ethnic minorities, including Asian, Native Hawaiian, Pacific Islanders (ANHPI), on transplant waiting lists exceeds their representation in the general population. However, few ANHPI adults, including college students, are designated organ donors (DOD) on their driver’s licenses or in a donor registry. Taking advantage of college students’ frequent use of online technology, including social networking sites (e.g., Facebook) and social media sites (e.g., YouTube, Twitter), the research teamwill create an online intervention that leverages this technology to engage and educate ANHPI students about a range of topics related to organ donation (OD). These topics would “map onto” college courses on ethnic studies, ethics, anatomy, religion, etc. The intervention will provide culturally sensitive messages tailored to their interest in or wariness of organ donation and reframe students’ attitudes about OD and becoming a DOD. This project will test the impact of the online intervention tailored to college students at three universities in Hawaii via a multiple baseline delayed treatment design. The goals are to increase DOD rates in ANHPI college students and encourage peer discussions/interactions/advocacy about OD. Students and student organizations will participate in contests to win prizes (e.g., best term paper or 2-minute YouTube video on OD/DOD issues). Innovative online strategies that can engage, educate, and reframe DOD for college students, including those who find it unappealing (i.e., the “ick factor”) are clearly needed. Our online intervention will use the concept of “gamification” to turn college students’ learning and advocacy ideas surrounding OD/DOD into a contest with their peers.
