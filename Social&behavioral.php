		<tr>
		<th>Project Title:</th><td>	Increasing donor designation rates in teenagers: Effectiveness of a driver's education intervention</td>
	</tr>
</table>
The objective of this project is to evaluate the relative effectiveness of informational (IM), testimonial (TM), or blended (BM) organ donation video messaging with adolescents during scheduled driver’s education classes. Classes will be randomized to receive one of the three interventions and 450 adolescent participants will complete questionnaires at pre-intervention, post-intervention, and 1-week follow-up. The research team expects that adolescents in the IM and BM groups will show more robust effects of organ donation knowledge, attitudes, behavioral intention, and actual donor designation rates at the time of getting their driver’s license as compared to the IM adolescents. Additionally, they expect that the IM and BM interventions will have a more robust effect on adolescents communicating their donation intentions to their parent(s) and on parents’ willingness to authorize deceased donation for the adolescent. The proposed research is innovative because it examines an organ donation intervention in an underserved population (i.e., adolescents) with historically lower donor designation rates. This is an important population to target because it represents the first time they will be asked to join the donor registry and because individuals are unlikely to remove themselves from donor registries once already enrolled. Pairing organ donation education with driver’s education ensures that such messaging occurs in close proximity to the donation request at the RMV office. Findings from this study are expected to advance the science of organ donation messaging and to have a positive impact on organ donation designation rates in Massachusetts.
