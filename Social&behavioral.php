		<th>Project Title:</th><td>	Integrating organ donation messages into evidence-based programs</td>
</tr>
</table>
This research study examines the effectiveness of integrating an organ donation education component into existing evidence-based health education programs to increase the number of individuals who enroll on the Michigan Organ Donor Registry. For the purposes of this project, an additional session will be added to two evidence-based programs: the Stanford Chronic Disease Self Management Program (CDSMP) and the National Diabetes Prevention Program (DPP). Bonus sessions will be provided at week 7 for both of the evidence-based program workshops. Each of the participating workshops will be randomized into one of two groups. In the intervention group, the bonus session will consist of an organ donation education component as well as a healthy eating education component. The organ donation education component will be led by a donation advocate, a trained volunteer with a personal connection to organ donation. In the control group, the participants will receive a healthy eating/active living education component. The target population for this project is individuals participating in an evidence-based program (CDSMP or DPP) in five Michigan counties; many of these individuals will be over age 50, living with at least one chronic disease, and African American. The primary outcome measure for the project will be verified registrations on the Michigan Organ Donor Registry. This program is led by a consortium including the National Kidney Foundation of Michigan, Gift of Life Michigan and the University of Michigan School of Public Health.
