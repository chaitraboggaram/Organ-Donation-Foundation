<table border="2">
	<tr>
		<th>Grantee:</th><td>	Claremont Graduate University</td>
		</tr>
		<tr>
		<th>Project Title:</th><td>	Maximizing donor registrations among Hispanics: A positive psychology approach</td>
</tr>
</table>
The proposed studies take a positive psychological approach toward increasing donor registration rates. While positive affect has long been associated with increased prosocial behavior such as charitable donations, the common understanding of emotions has recently become more nuanced. Feelings such as elevation (e.g., uplifted, inspired), serenity, gratitude, and happiness are now widely accepted as distinct emotions, with distinguishable characteristics. The first study predicts that positive emotions, in general, will be associated with increased donor registration rates compared to approaches that do not elicit positive emotion. It is hypothesized that different positive emotions are associated with different levels of donor registration rates. Specifically, elevation is posited to lead to significantly higher registration rates than feelings of gratitude or serenity. The second study continues to explore the relationship between positive emotions and organ donor registration. Organ donor campaigns often focus on the number of people in need of a donated organ, or the number people who die each day as a result of the organ shortage. Other campaigns focus on the lives saved through organ donation. In line with the predictions in the first study about the influence of elevation, it is proposed that the latter approach (i.e., focusing on the recipients) will lead to greater registration rates than focusing on the number in need or the number of people who have passed away. It is further proposed that this feeling of elevation, and its impact on donor registration rates, will be maximized when the recipient’s good character is highlighted.
