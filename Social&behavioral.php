		<th>Project Title:</th><td>	Increasing organ donation in New York State through voter registration</td>
</tr>
</table>
This project proposes to increase the number of individuals who consent to deceased donation in New York State (NYS) by gaining a greater understanding of the decision making process in relation to registry enrollments via the voter registration process. The project represents a fully collaborative intervention between the New York Alliance for Donation, the four NYS organ procurement organizations (OPOs), three NYS eye and tissue banks, the University at Buffalo, and the League of Women Voters (a nonpartisan political organization that encourages informed and active participation in government through voting). In 2008, NYS became the first state to pass legislation requiring the inclusion of an organ and tissue donor enrollment section on voter registration forms, with phased implementation beginning in 2009. Since inception, nearly 125,000 individuals have enrolled in the New York State Donate Life Organ and Tissue Donor Registry via voter registration forms, representing nearly 4% of cumulative enrollments to date. In 2013, over 15% of all registry enrollments were made via voter registration forms, significantly decreasing the percentage of enrollments made via motor vehicle offices seen in prior years. Individuals will be targeted for enrollment utilizing standard NYS voter registration forms at community-based voter registration drives. The project aims to increase by 5% the expected average enrollment gain in the state registry and to increase by 34% the percentage of individuals who enroll in the registry using the voter registration process.

<big><b><h4>Fiscal Year 2013 Grantees</h4></b></big>
<table border="2">
	<tr>
		<th>Grantee:</th><td>	The Research Foundation for the State University of New York on behalf of University at Buffalo</td>
		</tr>
		<tr>
		<th>Project Title:</th><td>	A Positive Deviance Approach to Increasing Familial Consent Rates</td>
</tr>
</table>
This project addresses the documented negative association between Organ Procurement Coordinators’ (OPCs) familial consent rates and racial/ethnic diversity of donation service area. The Positive Deviance (PD) approach provides a theoretical framework for the project. PD indicates that within any community there exist individuals who enact specific behaviors that allow them to thrive despite surrounding circumstances. The project will identify novel communication behaviors employed by OPCs who maintain high consent rates despite being located in racially/ethnically diverse areas (i.e., PD behaviors) and will disseminate such strategies among participating Organ Procurement Organizations (OPOs) toward the goal of improving familial communication and subsequent consent rates. The PD approach encourages OPCs to be active participants in each stage of intervention development, implementation, and evaluation. During the first year of the project, OPCs at 11 participating OPOs will complete interviews designed to identify PD behaviors. A proportion of these OPCs will also participate in a collaborative workshop designed to clarify the meaning of identified PD behaviors and will assist in creating an educational video for dissemination across participating OPOs that discusses and demonstrates such behaviors. During years two and three of the project, participating OPCs will complete educational sessions and refresher courses that employ a variety of active learning methods to increase knowledge of PD behaviors. Project success will be tracked by measuring OPCs’ quarterly consent rates and by examining the association between OPCs’ self-reported use of PD behaviors with familial consent and case characteristics.
