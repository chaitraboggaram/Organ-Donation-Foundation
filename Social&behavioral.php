This study will evaluate the independent and combined effects of two organ donation education interventions on consent for donation among adolescents and their parents. High school students from northeastern Ohio will participate in 4-hour multi-modality education summits to educate them about donation and ways to discuss donation with their parents. Adolescents are an important group for organ donation efforts as they have not yet applied for a driver’s license and represent the donors of the future. Yet many organ donation interventions have not targeted adolescents. This research team hypothesizes that adolescents exposed to tailored organ donation interventions will be more willing than non-exposed adolescents to consent to donation and more comfortable discussing donation with their parents and caregivers.
