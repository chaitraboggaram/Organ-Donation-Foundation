This project seeks to increase the number of African American adults who go online to designate their consent to become organ donors in the Georgia state donor registry. This will be accomplished by creating a web-based version of an existing culturally-sensitive intervention (Project ACTS: About Choices in Transplantation and Sharing) to address barriers to organ and tissue donation among African Americans. This web-based version of the intervention will include five 3-minute video segments addressing commonly held negative beliefs about organ and tissue donation. Next, the research team will launch the Project ACTS II website and identify and train online participant recruiters to recruit 800 study participants into the study. Half of the participants will be randomized to view the control website (Donate Life, Georgia) whereas the other half will be randomized to view the Project ACTS II videos on the project website. Website usage statistics will be gathered to conduct a process evaluation. The outcome evaluation will test whether individuals visiting the ACTS II website demonstrate: a) significantly greater donation-related knowledge, b) more favorable attitudes and beliefs towards organ and tissue donation, and c) stronger intentions to join the state donor registry at immediate post-test compared to control participants as determined by baseline and immediate follow-up measures. The team will also test whether individuals visiting the ACTS II website are significantly more likely to join the Georgia state donor registry than control participants by the end of the study as determined by Donate Life Georgia registry data.
