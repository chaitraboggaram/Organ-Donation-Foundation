<h1>Research Reports</h1>
The four reports below are available to provide assistance to individuals aspiring to implement, evaluate, and/or conduct rigorous research studies on public outreach campaigns to promote organ and tissue donation. Select each report for further information.
<h4>2012 National Survey of Organ Donation Attitudes and Behaviors</h4>
This document reports the results of a 2012 national survey of the American public's attitudes and behaviors related to organ donation.  Among the issues addressed by the survey are: willingness to donate organs; willingness to donate hands and face; having registered as an organ donor; attitudes toward financial incentives for organ donation; and whether would-be donors are more interested in having their organs transplanted locally or used for the most medically needy regardless of geographic location.  Because many of the items in the 2012 survey instrument are identical to items used in a 2005 national survey, the analyses of these issues show change over time.  Both surveys were conducted for HRSA by The Gallup Organization.
<h4>Report on the Research on Social and Behavioral Interventions to Increase Organ Donation Grant Program, 1999-2004</h4>
This report provides a summary of information gleaned from DoT grant projects funded in fiscal years 1999, 2000, and 2001 which submitted final reports by December, 2005 containing sufficient evaluation procedures that the level of success of the program could be assessed. Also integrated into the report is solid preliminary data from ongoing reports where available.
<h4>Promoting Organ Donation through Public Education Campaigns: A Random-Effects Meta-Analysis</h4>
This report reviews published studies, convention papers and unpublished grant reports to estimate the combined effect of public education campaigns on individuals' attitudes and behaviors toward organ donation. Outcome measures of effect included signing rates, discussion about donation, and attitudes toward donation.
<h4>Measures and Methods Research Report </h4>
This report reviews measures and research methods related to the scientific study of organ donation in the social, educational, and behavioral sciences. The primary goal of the report is to provide a resource for future outreach endeavors to increase organ donation. (In progress.)
