<br><br>Irish Kidney Association has resolved important organ donation issues including shortage of surgeons and the less than ideal hospital conditions driving young doctors abroad.
<br><br>MOHAN Foundation was invited by Mr. Sandeep, student of IIIT Basar Campus, to conduct organ donation awareness session on February 19, 2016. This session was organized as a part of community service initiative called "AAYUSH" by the students and alumni of IIIT Basar.
<br><br>National Organ Donation Day was celebrated at Velalar College of Engineering & Technology Erode on 27th November 2014. Erode Spice Round Table-211 initiated a sensitization programme for the college students. Mr S. Mohanraj Chairman Erode Spice Round Table invited MOHAN Foundation to give an awareness talk on Organ Donation. Mr. Prakash K. Transplant Coordinator from MOHAN Foundation spoke about the importance of organ donation in the programme. The programme began with a welcome address by Mr. S. Mohanraj, he explained the importance of event.
<br><br>Mr. Prakash K. spoke about the Concepts of Organ Donation. The following topics were covered during the talk:
<ul>
<li>Activities of MOHAN Foundation across the country to strengthen the Deceased Organ Donation programme</li>
<li>Types of Organ Donation - Live and Deceased Donation</li>
<li>Difference between Brain death and Coma</li>
<li>Importance of the consent from the family</li>
<li>Importance of Donor card </li>
<li>Tissues and organs  that can be donated</li>
<li>Transplant Coordinators Training Programme</li>
<li>Importance of Donor Card</li>
</ul>
<br><br>Totally 500 staff and students  of Biomedical Engineering participated in the programme and at the end of the session the staff indicated that they would like all the students in the different departments to benefit from the  lecture. Dr. M. Jeyaraman, the principal was happily sharing that this was the first college promoted organ donation in Erode. Later in the programme Tr. Christopher Aravinth, Area Chairman Round table India gave a vote of thanks and encouraged MOHAN Foundation for wonderful work.

