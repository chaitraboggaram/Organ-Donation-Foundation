<br><br>The 100 odd students and faculty of College of Education at Vinayaka Mission University, Pondicherry celebrated the International Women's Day on 20th March 2015 as they had exams on 8th March.  Mrs. Lalitha Raghuram, Country Director of MOHAN Foundation was invited as "Chief Guest" for the event. 
<br><br>Mrs. Raghuram spoke on the power of women, and what she can do to improve the society.  She spoke in Tamil, and encouraged the students to do their bit to the society in the area of conservation of natural resources like water, electricity, trees etc.  She urged the students to act as a catalyst in educating at least one girl child in their life time. 
<br><br>Mrs. Raghuram made a presentation on deceased organ donation, and distributed brochures on organ donation.  All the students enjoyed the interactive session and participated actively.  Several students picked up Donor Card voluntarily.  Dr. Govindarajan, Principal of the College spoke on the occasion and urged the students to be responsible citizens.  
