<h2><b>Why Minority Donors Are Needed</b></h2>
	<big>People of all ages, races, and ethnicities can save and enhance lives by donating their organs, eyes and tissues. Organ and tissue transplants are needed by people from every area of our nation.
	<br><br>Some of the conditions that can result in organ failure are best treated through transplantation; others can be treated only by this lifesaving procedure.
	<br><br>People of most races and ethnicities in the U.S. donate in proportion to their representation in the population. The need for transplant in some groups, however, is disproportionately high, frequently due to a high incidence of conditions such as high blood pressure or diabetes, both of which can lead to the need for a kidney transplant.
	<br><br>For example, African Americans, Asians and Pacific Islanders, and Hispanics/Latinos are three times more likely than Caucasians to suffer from end-stage renal (kidney) disease, often as the result of high blood pressure and other conditions that can damage the kidneys. Almost 34 percent of the more than 101,000 people on the national waiting list for a kidney transplant are African American.
	<br><br>Although organs are not matched according to race/ethnicity, and people of different races frequently match one another, all individuals waiting for an organ transplant will have a better chance of receiving one if there are large numbers of donors from their racial/ethnic background. This is because compatible blood types and tissue markers—critical qualities for donor/recipient matching—are more likely to be found among members of the same ethnicity. A greater diversity of donors may potentially increase access to transplantation for everyone.
