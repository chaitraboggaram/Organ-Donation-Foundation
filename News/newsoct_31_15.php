<big><big><b>Mumbai Records Its 36th Cadaver Donation of the Year Amidst Low Numbers</b></big></big><br><br>
<big><br>Mumbai has not fared well in cadaver donations. In 2012, it reported only 27 cadaver donations. The city reported 24 and 41 cadaver donations in 2013 and 2014 respectively.
<img src="" border="2" align="right">
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspRecently it received a cadaver donation from a 16-year-old girl who died due to a head injury. This was the 36th cadaver donation of the year.
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe cadaver donation was done at the BYL Nair Hospital, a public hospital in Mumbai. The girl had fallen from a terrace and slipped into coma. Her father gave consent to donate her kidneys.
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe girl saved the lives of two women who were on dialysis. One of her kidney was transplanted to a 31-year-old woman in Jupiter Hospital, while the other kidney went to a 49-year-old woman in Bombay Hospital.
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspWhile Mumbai still remains unstable in producing cadaver donations, other cities like Chennai has managed over 80 cadaver donations annually. More public hospitals in Mumbai are only contributing to organ donations when compared to the public hospitals.
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp"However, for a public hospital such as Nair, they should be reporting more than 12 brain deaths in a month and not in the entire year," said a doctor from the hospital, adding that if hospitals start identifying and reporting brain deaths, the number of organ transplants will automatically increase.
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp"The counselors can convince at least some of these families to give consent. But the organ transplant program is faltering at this basic step," said a senior doctor from Nair Hospital.
