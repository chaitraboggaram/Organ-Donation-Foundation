<big><br>Almost all human organs donated for transplant in China go unused even after years of controversy about the use of body parts from executed prisoners.
<img src="" border="2" align="right">
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspAuthorities expected to have more than 2,500 organ donors this year, the Beijing Youth Daily said, citing Huang Jiefu, head of the China Organ Donation Committee and a former vice health minister.
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThat could "technically" make possible 2,500 heart transplants and 5,000 lung transplants, he told the paper, but only just over 100 heart transplants have been carried out since January, and a similar numbers of lung transplants. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp"On the one side there is a shortage, on the other side there is waste," he was quoted as saying. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe paper blamed slow transport and poor co-ordination for the losses. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspIn contrast, more than 3,300 patients benefited from the organs of 1,282 deceased donors in Britain, according to the most recent full-year data from the National Health Service. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspOrgan donations in China are limited as many of its 1.37 billion people believe they will be reincarnated after death and so feel the need to keep a complete body. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspConsistently high demand has created incentives for forced donations and illegal sales, with overseas rights groups long condemning the harvesting of organs from executed prisoners. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspChina says it banned the practice from the beginning of this year, although some international medical groups suspect that prisoners may be reclassified as voluntary donors to get around the restriction. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspHuang insisted that "not a single organ" within the country's legal donation system was taken from death row prisoners. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp"While the legal system is not perfectly sound it is a false proposition in itself to ask a death-row inmate whether he is willing to donate as a citizen," he told the paper.	
