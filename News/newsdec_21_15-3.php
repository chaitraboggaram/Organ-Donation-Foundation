<big>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe University of California San Francisco Medical Center has shut down it's live donor program after a man donor passed away last month after providing a kidney to a patient at the hospital in October. UCSF officials are now investigating the cause of his death. 
<img src="" border="2" align="left">
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe medical center has suspended all the kidney transplants while the investigation takes place. According to the hospital officials, the recipient of the deceased man's kidney is reportedly healthy. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspRecipients of kidney transplants obtain their kidneys from dead donors. But the results are better when receiving the organ from a living donor. The risk of death of a living kidney donor is about 0.03 percent or 3 for every 10,000 donations. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspAlthough UCSF has suspended kidney transplant surgeries from living donors, kidneys that have already been taken out will continue to be provided to the recipients. UCSF will also continue to perform transplants from deceased individuals. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspMedical Director, Dr. Steven Katznelson, said, "Hospital officials worry about the risk of death every day. Even for a healthy person who goes under general anesthesia, there's always a risk". 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp"The safety and well-being of our patients are our top priority, and every effort is being made to understand what happened. We are deeply saddened by this tragic event‚Äù, UCSF said in a statement. 
