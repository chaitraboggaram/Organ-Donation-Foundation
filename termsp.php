<br><br><br><br><b>Pancreas-</b>Long, irregularly shaped gland that lies behind the stomach. Some glands in the pancreas secrete insulin. Pancreas transplants give patients with diabetes a chance to become independent of insulin injections. In addition to insulin, the pancreas secretes digestive enzymes (into the small intestine) that aid in the digestion of proteins, carbohydrates, and fats.
