		<th>Grantee:</th><td>Beth Israel Deaconess Medical Center</td>
	</tr>
	<tr>
		<th>Project Title:</th><td>	A DMV-Based Intervention to Increase Donor Registrations: The new England Dissemination Project</td>
	</tr>
</table>
The long-term goal of this program is to increase rates of organ donation by substantially increasing the proportion of licensed drivers in New England who are registered organ donors. The objective of this project is to further disseminate and evaluate a targeted division of motor vehicle (DMV) office campaign to increase donor registry enrollment in New England. Based upon a HRSA study that demonstrated the effectiveness of the DMV-based intervention, this project will evaluate its dissemination in other regions of New England as a logical extension of the research program. The central hypothesis is that the educational outreach will yield higher rates of donor registry enrollment by DMV customers (compared to pre-intervention rates) in Rhode Island. The rationale for the proposed research is that it is important to establish whether effective DMV-based education strategies can be disseminated with the same level of effectiveness in other states with different population parameters and DMV processes. To evaluate our central hypothesis, two specific aims will be pursued: 1) To determine the effectiveness of an educational outreach program in DMV offices in Rhode Island, and 2) To evaluate the impact of the educational outreach program on organ donor registration rates in census tracts with low donation rates. The expected outcomes of this study will yield valuable information regarding the dissemination of effective DMV-based strategies in increasing donor registry enrollment in Rhode Island.
