		<th>Grantee:</th><td>University of Illinois at Urbana-Champaign</td>
	</tr>
	<tr>
		<th>Project Title:</th><td>A randomized controlled experiment to evaluate a multi-message, phased driver facilities campaign</td>
	</tr>
</table>
<br>The current project utilizes a multi-message, phased driver facility campaign featuring (a) clerk training, (b) point-of-decision materials, and (c) video monitors strategically placed inside Chicago driver facilities to disseminate organ and tissue donation messages.  By varying the order of the intervention arms, the research team will be able to test the effectiveness of each arm individually as well as in conjunction with each of the other components.  By utilizing both control and intervention driver facilities, the team can determine if organ donation registration trends observed during the eight-month campaign are due to the intervention arms or an environmental confound or secular trend.  In addition to testing the effectiveness of each intervention arm on organ donor registrations, the evaluation strategy will empirically account for exposure to each intervention arm.  Perhaps more importantly, the current driver facilities campaign will reach approximately 82% of African-American and 90% of Hispanic Illinois residents. 
