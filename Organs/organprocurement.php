<h1 style="color: #191970;"><big><big><b>Organ Procurement Organizations</b></big></big></h1>
<br><br>There are 58 organ procurement organizations (OPOs) in the United States. OPOs are responsible for two main functions within their designated service area:<ol><li>Increasing the number of registered donors</li><li> Coordinating the donation process when actual donors become available.</li></ol>
<br><br>To increase donor sign-up, OPOs may implement any number of community outreach activities such as sponsoring advertising campaigns, programs in schools, worksites, or faith institutions, disseminating print and electronic materials, etc.
<br><br>When actual donors become available, OPOs evaluate the potential donors, check the deceased's state donor registry, discuss donation with family members, contact the OPTN and run a match list, and arrange for the recovery and transport of donated organs. They also provide bereavement support for donor families and volunteer opportunities for interested individuals.
<br><br>OPOs employ a variety of staff including procurement coordinators, requestors, specialists in public relations, communication, and health education, as well as administrative personnel.
<br><br>OPOs must be certified by the Centers for Medicare and Medicaid Services (CMS) and abide by CMS regulations. By federal law, all OPOs must be members of the Organ Procurement and Transplantation Network (OPTN). All OPOs are members of the Association of Organ Procurement Organizations.
