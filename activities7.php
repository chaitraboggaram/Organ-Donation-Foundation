<br><br>The Civil Engineering Students' Association (CESA) and Rotaract Club of Nagpur Institute of Technology (NIT) organized an Organ Donation Awareness Session for the entire faculty members and students of Third Year B. E. on 15th March 2016 at the Seminar hall of the Institute. Dr. Ravi Wankhede Head of MOHAN Foundation-Nagpur Center was the keynote speaker.
<br><br>Seated on the dais were Dr. Surendra Gole, the Principal, Prof. M N. Umare, Head Civil Engineering, Prof. Rahul Kadam, Head of NSS Unit and Prof. Ravikant Vairagade, in-charge of Rotaract Club under the Rotary Club of Nagpur. Dr. Surendra Gole spoke on the need to understand the subject which was of vital importance. Prof. Rahul Kadam said, the NSS was formed for inculcating the social responsibilities of College students and organ donation is one important social subject which needs to be understood.
<br><br>Dr. Ravi Wankhede gave a PPP on the subject elaborating on the historical, legal, social and humanitarian aspects of organ donation. He said it is of importance to for College students to know why one should donate organs and tissues, especially when it is not anymore required after death. He said there is a huge demand for kidneys in India yes less than 1% people get a transplant. He said there are various myths as to why people do not donate organs, such unawareness, religious myths and a general social apathy amongst Indians. He said college students need to overcome all such deterrents and bring a change in the society and thereby bring a good name of the country.
<br><br>Transplant Coordinators Ms. Kanchan and  Mr. Bulu worked hard to arrange the talk. The staff and students assured to spread the knowledge gained amongst their family and friends. Over 150 staff and students were present in the auditorium.
