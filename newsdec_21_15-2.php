<big><big><b>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspGreen Corridor Helps 35-Year-Old Man To Get Heart From Brain Dead</b></big></big><br><br>
<br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspA green corridor was created in Bengaluru to facilitate the transportation of the live heart of an accident victim, who was declared brain dead.
<img src="" border="2" align="right">
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe 30-km journey from BGS Hospital, Kengeri, to Narayana Hrudayalaya near Electronics City was undertaken swiftly in just 32 minutes on December 20. This is the seventh such green corridor done this year at Narayana Health’s flagship cardiac hospital. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe donor, Kiran Kumar was a 19-year-old college student, who had met with an accident when he was returning from Nandi Hills with his friends. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspHe was treated at BGS Global Hospital and the doctors declared him brain dead on December 19. His father agreed to donate his son's organs. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspKiran's father, Prabhu Kumar, said, “I lost my wife a year ago and now, my younger son is no more. I am yet to come to grips with this loss. If someone can get a new lease of life through Kiran, I think he has done his part.”
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe recipient is a 35-year-old man from Delhi. He was suffering from Restrictive Cardiomyopathy and had been waiting for a donor for the last five months. 
<br><br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe deceased's eyes have been sent to Narayana Nethralaya. His liver is being transplanted on a 54-year-old man from Bengaluru. His liver will be transplanted on a 49-year-old from Bengaluru.

