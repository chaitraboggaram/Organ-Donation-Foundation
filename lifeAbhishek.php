<h2 style="color: blue;">Life Stories: Biology Class Inspires a Life-long Organ Donation Advocate</h2>
<big>Even in high school, Abhishek T. was an inquisitive student who wanted to learn more about the subjects that he studied. His curiosity was especially piqued during a 10th grade biology class lesson in which he learned about how stem cell therapy could serve as an alternative to organ donation. That same year, he applied to get his learnerâ€™s permit and discovered that only 8 percent of Californians were registered to be organ, eye, and tissue donors.  
<br><br>After doing further research and interning for the California Transplant Donor Network, Abhishek felt that his purpose in life was to study medicine. He also was determined to help his entire family, including his grandmother in India, sign up to become donors.
<br><br>Today, as a senior in high school, Abhishek participates in several activities to raise awareness of organ, eye, and tissue donation in the Indian community. One of these is Sevathon, a community event that raises awareness for non-profit organizations.
<br><br>During this event, which attracts more than 2,000 participants, literature about organ donation is distributed, and people are encouraged to register online to be organ and tissue donors. Although some participants have signed up, many are still wary about registering. To encourage people to register, Abhishek talks to them, asking them what they know about organ donation, educating them about the process, and especially helping to dispel the common myths surrounding donation.
<br><br>"Karma is a big deal. If you sign up as an organ donor, it would be a good deed." - Abhishek T.
<br><br>When asked why it is important to register to be an organ, eye, and tissue donor, Abhishek says, "One person can save eight lives and improve 50 others. Karma is a big deal, and if you sign up as an organ donor, it would be a good deed."
