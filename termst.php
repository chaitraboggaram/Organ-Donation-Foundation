<br><br><br><br><b>Tendon-</b>A tough, flexible band of fibrous tissue that connects muscles to bones. The skeletal muscles move the bones for walking, jumping, lifting, etc. by contracting and pulling the bones. The tendon attaches to the muscle and bone and transmits the force of muscle contraction to the bone. Tendons can be transplanted. See Connective Tissue.
<br><br><b>Tissue-</b>A body part consisting of similar cells that perform a special function. Examples of tissues that can be transplanted are bones, corneas, heart valves, ligaments, veins, and tendons.
<br><br><b>Tissue Typing-</b>A procedure in which the tissues of a prospective donor and recipient are tested to identify the human leukocyte antigens (HLA). See HLA.
<br><br><b>Transplantation-</b>The transfer of cells (eg. stem cells), tissue, or organs from one person to another.
<br><br><b>Transplantation, Allogenic (allograft)-</b>Transplantation between genetically different members of the same species (not identical twins)
<br><br><b>Transplantation, Autologous-</b>Receiving a transplant of one's own cell or tissues. This type of transplantation can be used to repair or replace damaged tissue. For example, autologous bone marrow transplantation permits the use of strong cancer therapies that can damage bone marrow. Bone marrow is removed prior to treatment and once the treatment is completed marrow that has not been affected by the therapy is transplanted back into the patient.
<br><br><b>Transplant Coordinator-</b>A transplant center staff member responsible for managing the care and progress of potential transplant recipients before, during and after the transplantation.
<br><br><b>Transplant Recipient-</b>A person who has received a tissue or organ transplant.
