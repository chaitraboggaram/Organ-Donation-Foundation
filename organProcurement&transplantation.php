<br><br><b>Note:</b> Policies and bylaws must be forwarded for review and approval by the Secretary of the U.S. Department of Health and Human Services (HHS) to become binding under the authority of federal regulation. The current OPTN policies and bylaws have not undergone this process and are voluntary. However, the requirements specified in the OPTN Final Rule are mandatory and enforceable by HHS.
<h4 style="color: #191970;">Policy Development</h4>
Continuing scientific and technological advances in organ transplantation require ongoing allocation policy refinements. Policies evolve to ensure that patients benefit from the best possible solutions.
