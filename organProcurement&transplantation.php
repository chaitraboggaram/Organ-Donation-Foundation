<h1 style="color: #191970;"><big><big><b>Organ Procurement and Transplantation Network Policies and Reports</b></big></big></h1>
<br><br>The Organ Procurement and Transplantation Network (OPTN) policies and bylaws govern the procedural aspects of policy development, allocation of donated organs, and the collection of transplant data nationwide. The public is included in the process through solicitation of feedback through a public comment process.
<br><br>OPTN committees and the OPTN board of directors develop policies, and the United Network for Organ Sharing (UNOS), which holds the federal contract to operate the OPTN, is responsible for coordinating committee and board actions.
