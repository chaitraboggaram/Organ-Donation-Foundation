<br><br><big style="color: blue;"><big><b> More About Statistics</b></big></big>
<br><br>Statistics can sometimes be overwhelming and difficult to understand. One thing to remember is that every number in the statistic you view is a person-a person who either needs your help and is waiting for a lifesaving transplant or a person who has left a lasting legacy through organ and tissue donation.
<br><br>Either way, each number represents a life, a mom, a dad, a brother, a sister or a child-someone who is important to someone else, maybe even you.
<br><br>Statistics change. Some change day to day and some can even change minute to minute. So you may see different numbers each time you return to organdonor.gov or some of the other sites linked from here. You may ask why this happens. There are several reasons:
<ul>
<li>One of the most confusing statistics is the number of persons waiting for a transplant. Patients are allowed to register at multiple transplant centers, so you may see a higher number if you count "registrations" rather than "candidates."</li>
<li>Additionally, one of the great things that may happen is that donations and transplantations may be taking place at any time, so while the waiting list might continue to grow the number of donors may also rise.</li>
<li>The reality is that the number of candidates waiting continues to dwarf the number of donor organs available, and only you can change this-by registering as an organ, eye, and tissue donor and encouraging others to do so as well.</li>
</ul>
