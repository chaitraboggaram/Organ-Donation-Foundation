<h1 style="color: #8B0000">The Need Is Real: Data</h1>
<br><big>Organ transplantation has become an accepted medical treatment for end-stage organ failure. But only you can help make it happen. Here are some important facts about donation:
<br><ul>
<li>During your visit to organdonor.gov someone may have been added to the waiting list. It happens every 10 minutes.</li>
<li>Each day, an average of 79 people receive organ transplants. However, an average of 22 people die each day waiting for transplants that can't take place because of the shortage of donated organs.</li>
<li>People of every age give and receive organ donations. In 2014, 29,532 people received organ transplants. Below is the number of recipients by age group followed by the number who received organs from deceased and living donors: </li>
<ul>
<li>< 1 Year Old: 280 (261 deceased, 19 living donors)</li>
<li>1 - 5 Years: 503 (417 deceased, 86 living donors)</li>
<li>6 - 10 Years: 284 (226 deceased, 58 living donors)</li>
<li>11 - 17 Years: 728 (594 deceased, 134 living donors)</li>
<li>18 - 34 Years: 3,148 (2,087 deceased, 1,061 living donors)</li>
<li>35 - 49 Years: 6,407   (4,780 deceased, 1,627 living donors)</li>
<li>50 - 64 Years: 12,791 (10,758 deceased, 2,033 living donors)</li>
<li>65+ Years: 5,391 (4,592 deceased, 799 living donors)</li>
</ul>
<br><li>As of December 4, 2012, the percentage of recipients who were still living 5-years after their transplant is noted below for kidney, heart, liver, and lung. For more information, click here. </li>
