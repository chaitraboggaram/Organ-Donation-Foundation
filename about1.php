	</ul>
	&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspIt also involves to transplantation of tissues such as,
	<ul>
		<li>Bones</li>
		<li>Muscoloskeletal grafts</li>
		<li>Cornea</li>
		<li>Skin</li>
		<li>Heart valves</li>
		<li>Nerves</li>
		<li>Veins</li>
	</ul>
	&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspIt is one of the most complex and challenging areas of medicine, because of the ever-present  risk that the recipient body rejects the transplant, making the removal necessary.
	<br><br><br><br>
	<h1>Benefits and Outcomes of Organ Transplant</h1>
	<br>
	&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspOrgan transplant is the last possibility to address a state of organ failure. Kidney for instance, is the most frequently carried out organ transplant worldwide, and it is considered the best treatment for its cost effectiveness and life quality prospects it restores.
	&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspOrgan transplantation requires long term health evaluation of the patient. Only academic communities and medical scientists have the right  to monitor the outcomes of transplants and regulate donations.
