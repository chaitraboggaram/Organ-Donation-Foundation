	<h1>Three Essential Processes</h1>
	&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspIn modern times, doctors and patients face an enormous demand for transplants which has long surpassed the supply of organs. Patients must wait a long time, years in some cases, for a chance to get hold of a donated organ. Thatâ€™s why scientists are working along with politicians to solve this problem.
	&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspOrgan distribution is therefore the first essential step, followed by the transplant surgery and the follow-up or post-surgery recovery.
	
	<h1>Evaluation Process</h1>
	&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe following are some components of the transplant evaluation process:
	<ol>
		<li>Psychological evaluation</li>
		&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspIn which the medical team assesses significant psychological and social issues such as stress, financial situation and family support.
		<li>Blood tests</li>
		&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspEssential in the selection process to joining the donor's list. They are performed to determine donor match, priority in the list and to improve chances against organ rejection.
		<li>Diagnosis</li> 
		&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspTo assess health status. Includes X-rays, ultrasound, biopsy, dental examinations, among other diagnostic tests depending on the transplant surgery required.
	</ol>
	
	<h1>Organ Distribution</h1>
	&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspWhen a particular organ fails, transplant can be the only chance for the patient. For procedures like kidney and liver transplant, a willing donor might be found among family members or friends. A very small number of transplants come from people donating as a result of a good, Samaritan gesture. Nevertheless, there is still the necessity of being appropriate for donor-recipient match, a process of selection achieved through serotyping. Then it is possible to proceed with the surgery state.
	&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspPatients must find a transplant team or a group of organ surgeons and health professionals, who decide if the patient is a good candidate, based on the attitude, psychological state, medical history and other factors, to be included in the national waiting list for transplantation.
	&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspWhen the organ becomes available, based on the criteria of all relevant information, a recipient, the best match for the organ is chosen. Then the hospital prepares for surgery.
	 
	<h1>Surgical Procedure</h1>
	&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe fully anesthetized patient is injected with anticoagulant to keep the blood from clotting during the transplantation procedure. Doctors connect the hearth-lung machine, in the case of a heart transplant, or other life-support devices to enable the surgeon to remove the organ without disrupting body functions.
	&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspAfter the organ exchange, the patient is stitched and taken to recovery room, where they are expected to regain consciousness within a few hours. They might leave the hospital in less than a week.
	
	<h1>Three types of rejection</h1>
	&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspFollowing a transplant surgery, the following three types of rejection might occur:
	<ul>
		<li><b>Chronic rejection</b> - Might last months or years.</li>
		<li><b>Acute rejection</b> - A few days after transplant and it is the immune response to foreign matter.</li>
		<li><b>Hyperacute rejection</b> - As soon as the organ is connected to the new body.</li>
	</ul>
	
	<h1>Types of Transplant</h1>
	<ol>
		<li><b>Autograft</b> - transplant of tissue from one area of the body to another, using surplus tissue which is regenerative.</li>
		<li><b>Allograft</b> - transplant of tissue or organ between non-identical members of a species. This transplant might cause rejection due to genetic difference.</li>
		<li><b>Xenograft or xenotransplant</b> - a transplant from one species to another. Very risky due to rejection.</li>
		<li><b>Split transplants</b> - when an organ is divided between two recipients (liver transplant usually, liver being regenerative).</li>
		<li><b>Domino</b> - when both livers are to be replaced from patients suffering from cystic fibrosis.</li>
	</ol>
