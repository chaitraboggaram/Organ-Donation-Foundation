<br><br>On 20th March 2016, MOHAN Foundation was invited to give a talk at Sri Sri Working Women's Hostel of the University Women's Association at Pallikaranai. It was a part of their International Women's Day celebrations.   Dr. Hemal Kanvinde represented MOHAN Foundation.
<br><br>She elaborated on the medical, legal, social and humanitarian aspects of organ donation. She touched upon the various myths as to why people do not donate organs. She said youngsters need to overcome all deterrents and bring a change in the society and thereby keeping Tamil Nadu in the leading for organ donations.
<br><br>20 young girls, 6 members of the association and 4 staff of the hostel attended the event. 9 girls picked a donor card and supported the cause. 
