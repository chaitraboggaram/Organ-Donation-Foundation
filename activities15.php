<br><br>International Women's Day is celebrated on March 8 every year. In different regions the focus of the celebrations ranges from general celebration of respect, appreciation and love towards women for their economic, political and social achievements.
<br><br>On 8th March 2016, Trinity College of Engineering, Trinity Hill, Trivandrum, Kerala, conducted an awareness programme on Organ Donation for their students. This programme was organized NSS volunteers as a part of Women's Day celebration. They have done a mass programme on Empowering Women. On
<br><br>The awareness section began with a welcome address by the principal, Trinity College.  Ms. Kavitha. S, Marketing Coordinator was deputed from MOHAN Foundation to address the gathering.   She explained the importance of event and the noble cause to the 50  female students who attended the event. 
<br><br>The following topics were covered in her talks:
<ul>
<li>Activities of MOHAN Foundation Types of Organ Donation - Live and Deceased Donation</li>
<li>Difference between Brain death and Coma</li>
<li>Donor card  & Importance of the consent from the family</li>
<li>KNOS Deceased donor programme by Govt. of Kerala</li>
<li>Information sources - Helpline Number and Websites</li>
</ul>
<br>Many queries asked by the participants
<ol>
<li>Whether MOHAN Foundation can provide donor for needy people.</li>
<li>Difference between KNOS and MOHAN Foundation.</li>
<li> Whether MOHAN Foundation providing any financial assistance to the end stage organ failure patient.</li>
</ol>
<br>All students and faculty attended the awareness section supported organ donation by pledging their organ through signing donor card.
