<br><br>MOHAN Foundation conducted an awareness session on organ donation at Loyola College of Social Sciences, Trivandrum on February 19th, 2016.
<br><br>Loyola College of Social Sciences was founded by the Society of Jesus (Jesuits) in 1963, the College offers courses in M A (Sociology), MSW (Masters in Social Work), MAHRM (Masters in Human Resource Management) and MSc Counselling Psychology. The College is affiliated to the University of Kerala and is a recognized Research Centre of the University. The Department of Social Work was the pioneering professional department under the University of Kerala to offer a Master's Degree in Social Work. The department has been consistently ranked at the 5th position in various surveys of professional colleges in India.
<br><br>The talk was coordinated and conducted by Ms. Mareena Thomas, Programme Officer ( Delhi-NCR) who is an alumna of the college. Dr. Sonny Jose, Head of the Department of Social Work, organised the session.
<br><br>With Kerala doing well in organ donation, many of the students were familiar with the concept of organ donation.  However, most of them were not aware that only in the case of brain dead one can donate organs. Through the presentation it was further clarified that only corneas are retrieved after a natural or cardiac death. They were also able to understand better on how corneal donations can be easily carried out.
<br><br>Mareena also discussed the job opportunities that the students, especially the MSW students, would have as a transplant coordinator. She explained that according to Transplant of Humans Organs and Tissue Act,2011 it was mandatory for any hospital that conducts transplantation to have a transplant coordinator. Dr. Jose expressed his desire to involves students from Loyola College in a certified training to help them to be transplant coordinators
<br><br>"Will we be able to know who is getting our organs?" was a very justified question asked by a student. Mareena clarified that due to ethical and other considerations, the identity of the donor and recipients is kept confidential but there is a fair and transparent system in place for sharing of organs that is governed by the state.
<br><br>There were 105 participants in all and 65 donor cards were picked up. Many students picked more than one donor card to motivate and enroll their own family members.
