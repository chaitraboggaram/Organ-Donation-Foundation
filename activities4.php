<br><br>MOHAN Foundation was invited by Mr. Sandeep, student of IIIT Basar Campus, to conduct organ donation awareness session on February 19, 2016. This session was organized as a part of community service initiative called "AAYUSH" by the students and alumni of IIIT Basar.
<br><br>MOHAN Foundation was invited by Mr. Sandeep, student of IIIT Basar is a part of Rajiv Gandhi University of Knowledge Technologies established exclusively to provide better educational opportunities to rural students with an objective to use advances in learning sciences and explore the use of modern cognitive tools in education and learning by providing ICT tools to the rural youth.
<br><br>Dr. Koti Reddy explained the purpose, necessity, importance, complex issues in deceased organ donation, concept of brain death, cultural and religious barriers, THO Act and myths related to organ donation and the key role played by youth in taking forward the noble cause of organ donation.
<br><br>There were about 75 college students and faculty that attended the program and all of them were distributed donor cards and information brochures. The college students positively to the idea and have promised to spread awareness about organ donation and include it in their community initiative called AAYUSH. They have also promised to spread awareness on E-Donor Card and provide space for online pledging during their TECH FEST planned next month.
<br><br>We thank Mr. Sandeep, Ms. Ramya and Vice Chancellor of IIIT Basar for their kind support in organizing this awareness session and also thank all the students for their support in making this program a grand success.
