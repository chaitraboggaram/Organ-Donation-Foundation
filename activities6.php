<br><br>NOTTO (National Organ & Tissue Transplant Organization) in partnership with MOHAN Foundation conducted the second Transplant Coordinators' Training Program from March 1-5, 2016 at NOTTO office, Safdarjung Hospital, New Delhi. The first training was also conducted in partnership with the foundation last year in the month of July.
<br><br>NOTTO is in the process of creating a national syllabus for Transplant Coordinators and the training program is an integral part of this curriculum that would help transplant coordinators in various transplant centres across the country to become effective coordinators as the success of organ donation and transplantation depends upon good counselling and coordination by transplant coordinators.
<br><br>34 participants of varied profiles like doctors, nurses, medical social workers, psychologists and quality managers from both private and government hospitals of Delhi-NCR attended the training. Both transplant and non transplant retrieval centre hospitals were short listed by NOTTO to send their delegates, to name a few: Army R & R, Safdarjung, BLK Super specialty, Sir Gangaram, Institute of Liver & Biliary Sciences, Rockland, Max Super specialty, Batra, LHMC & Sucheta Kriplani, Lal Bahadur Shastri, Moolchand, Rama Super specialty, Dr. Baba Sahib Ambedkar, Fortis and IP Apollo Hospital.
<br><ul>
<li>The five day training addressed the following areas:</li>
<li>Introduction to organ transplant</li>
<li>Concepts of organ donation</li>
<li>History of transplant</li>
<li>Various organisations (Govt & NGO's) working in the field of organ donation</li>
<li>National Organ Transplant Program</li>
<li>THOA Act & hospital based requirement</li>
<li>Brain death identification & certification</li>
<li>Basis of organ failure & transplantation (kidney/liver/heart)</li>
<li>Tissue Banking</li>
<li>Ethics in organ donation</li>
<li>Counselling (role & qnnualities of a Transplant Coordinator)</li>
</ul>
<br><br>The training was held by the trainers of MOHAN Foundation (Dr.Sunil Shroff, Ms Pallavi Kumar and Dr. Muneet Sahi) along with the support of other faculty from Safdarjung and other government and private hospitals. Films on various topics were screened and group activities like poster making and role play were also organised during the training.
<br><br>The training concluded with a valedictory function which was graced by Dr. Jagdish Prasad (DGHS, MoFHW, Govt. of India). He congratulated the participants for successfully completing their training and gave away the certificates. Some of the participants also shared their experience and gave feedback of the training.
