<br><br>On 24th February 2016 MFJCF conducted an awareness drive at Yagyavalkya Institute of Technology Sitapura, Jaipur organized by Yagyavalkya Institute of Technology and Rotary Club Jaipur Heights in Semimar hall of campus.
<br><br>MFJCF speaker Dr Poonam Gupta addressed students and faculty members and described concepts and values of organ donation. More than 200 students and faculty were there specially to get information about organ donation.
<br><br>After the talk some questions were asked by students like "How can I donate my Liver separately when I am alive?", and answers given them to by Dr Poonam Gupta. Everyone appreciated the efforts of MFJCF and 7 pledge form filled by them. 50 donor cards picked up by the students and five clicked photographs with the message board of “I SUPPORT ORGAN DONATION”.
<br><br>After the talk Rotary club Jaipur Heights District Governor Rtn Pradhuman Patni felicitated Dr. Poonam Gupta and appealed members to join hands with MFJCF for Noble Cause.  

