<br><br>In India, 90% of the people on the organ donation waiting list die - unable to obtain the necessary organs. The main culprits responsible are the many myths surrounding organ donation as well as general lack of awareness amongst the common people.
<br><br>Keeping all this in mind, we at Kshamta, the Social Responsibility Forum of NMIMS SPP SPTM, held an Organ Donation Drive Competition in association with MOHAN Foundation.
<br><br>The competition was held from 24th October to 2nd November. Around 14 students participated in the competition. The aim was to register any people as possible, as organ donors. The teams utilized social media tools such as Facebook, Twitter, Instagram and WhatsApp to spread the word and encourage people to register as organ donors.
<br><br>Team 5 (Shreya Savla   &  Rashmi Quamar)  was the clear winner with  57 registrations.  A total of 80 registrations were obtained by the teams throughout the entire week.  We would like to thank the teams for their enterprising nature and active participation, and also the people who took initiative to get themselves registered as organ donors.
<br><br>Winning Team - Report-Organ Donation Awareness Contest
<br><br>Relief from agony, anguish, trauma, throes, torture and torment is the cumulative result of something that as noble as organ donation can provide. It's a very selfless step towards the betterment and well-being of a probable unidentified human which makes the procedure all the more altruistic.
<br><br>The very first briefing of the competition of organ donation awareness made us extremely enthusiastic and ebullient about the prospect of being associated with its augustness. Right from the first day we started spreading word via our social media tool Whatsapp about Kshamta's new venture and gained a favourable response from our friends and their families. We encountered multiple queries regarding the intricacies and after effects of registering as an organ donor. Numerous colloquies with eager and zealous organ donors ensued with us getting a steady number of registrations. It was heart-warming to see so many people take an initiative to collect as many registrations as possible.
<br><br>The sheer thought of our feat concomitantly resulting into an uncomplicated and pain-less life made us feel contented and exultant. Such endeavours and projects invokes a feeling of immense joy and satisfaction in us.
<br><br>Team No-5: Shreya Savla  & Rashmi Quamar
