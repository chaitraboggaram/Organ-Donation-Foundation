<br><br>MOHAN Foundation, Nagpur Centre was invited on 2 March 2016 by Mrs. Tayade to conduct decease organ donation awareness talk to 20 teachers of St Ursula Girls High school, Civil Line, Nagpur. 
<br><br>Ms. Kanchan Shewde Transplant Coordinator of MOHAN Foundation, Nagpur center conducted the session. She gave information to teachers about:
<ol>
<li>What is Organ donation and type of donation?</li>
<li>Type of death, Natural death & Brain death.</li>
<li>Processes of Organ donation &Body donation.</li>
<li>Organ transplant Act.</li>
<li>Meaning of brain death.</li>
<li>Cause of brain death.</li>
<li>Difference between Natural death & Brain death.</li>
<li>Information about skin donation & eye donation.</li>
<li>Importance helpline number & website.</li>
</ol>
<br>After the talk, the participants asked the following question.
<ol>
<li>Whom to contact for Eye donation?</li>
<li>In Nagpur which organs are transplanted?</li>
<li>What is process of skin transplant?</li>
