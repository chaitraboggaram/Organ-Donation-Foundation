<br><br>Irish Kidney Association has resolved important organ donation issues including shortage of surgeons and the less than ideal hospital conditions driving young doctors abroad.
<br><br>MOHAN Foundation was invited by Mr. Sandeep, student of IIIT Basar Campus, to conduct organ donation awareness session on February 19, 2016. This session was organized as a part of community service initiative called "AAYUSH" by the students and alumni of IIIT Basar.
<br><br>MOHAN Foundation was invited by members Rotary Club of Udumalpet & IMA, Udumalpet  to give a talk on Organ Donation to their members. This initiative was taken by Rtn. PHF. P. Ravichandran, Dr. P. Senthilkumar, Mr. Nagaraj, Dr. Sathishkumar, Dr. Sundarajan. They event was organized on 31.05.2015, at IMA hall Udumalpet. Dr.Vijayanand Palaniswamy, Paediatric &Adult ICU Consultant  at GKNM represented MOHAN Foundation, as well as Dr. George Sundarajan, Dr. K. M Abdul Hasan were the resource persons for the program.
<br><br>Dr.Senthilkumar welcomed the speakers as well as the audience. Dr.George Sundarajan, Dr. K.M Abdul Hasan gave an overview about organ donation. 
<br><br>Dr.Vijayanand Palaniswamy addressed the gathering and spoke about the concepts of Brain Death and Organ Donation. He spoke on following topics:Importance and need of organ donation Types of donations - live and deceased donation Difference between Natural Death, Coma and Brain Death Organs and tissues that can be donated in cardiac death and brain death Time of preservation of Organs  
<br><br>Totally 50 members participated in the programme and all expressed their support for organ donation.

