<br><br>The postgraduate women's hostel at Shivaji University was the venue for a talk on organ donation on 18th December2015.  It was organised by Dr Niranjana Chavan, Prof and Chief Rector of the hostel.  Around 150 girls attended the talk along with the management  staff of the hostel - two rectors and the night warden. Dr. Hemal Kanvinde represented MOHAN Foundation and spoke on the concepts of organ donation.
<br><br>Dr Chavan introduced the speaker and the work of MOHAN Foundation. Dr Hemal talked about the need for donation, how and when it is done and how the young girls can promote the idea in their localities.  One student shared her experience as a younger sister of a kidney failure patient undergoing dialysis. This made a great impact on the listeners.
<br><br>32 students picked a donor card. Many were interested in becoming counselors and asked the practicality of the concept in the rural areas of Kolhapur.  At the end of the program Dr Asawari Jadhav  rector of the hostel,  thanked the speaker. 
