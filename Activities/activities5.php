<br><br>To commemorate the World Kidney Day, Paras Hospitals Gurgaon organised a press conference on March 09, 2016. MOHAN Foundation was invited to be a part of the press conference to spread the message on organ donation.
<br><br>On this occasion, Paras Hospitals announced the launch of the support group, “Utsah” for patients undergoing dialysis or awaiting a kidney transplant.
<br><br>Dr P N Gupta spoke about the rising incidence of kidney diseases in India and the role of dialysis. Many patients present on the occasion shared their experiences and challenges.
<br><br>Ms Pallavi Kumar spoke about the ever widening gap between the demand and supply of organs for transplants. She emphasized that organ donation is the need of the hour as the quality of life changes dramatically for those who undergo a transplant vis-à-vis those undergoing dialysis, hooked on to a machine 3 times a week.
<br><br>MOHAN Foundation also put up an awareness stall in the hospital lobby giving information to visitors on organ donation.
