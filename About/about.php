As you decide whether to donate an organ as a living donor, weigh the benefits and risks very seriously.
<br><br>It's important for you to get as much information as you can before making a decision. The transplant center should fully explain the organ donation process to you. You should also be assigned an independent donor advocate who will promote your medical rights.
<br><br>Make sure you ask a lot of questions throughout this process. It's important for you to fully understand the surgery and how becoming an organ donor might affect your future health.
<br><br>Finally, remember that this is your decision -- yours alone. Don't let anyone sway that decision. Even if a friend or loved one is very sick, you have to consider how donating an organ might affect your own life. Remember that even though the donation process has started, you have the right to stop it at any time if you change your mind.
