<h1>Healthful Habits</h1>
There are many reasons why people suffer end-stage organ failure and need an organ transplant and why others are not accepted as organ donors. Some reasons are totally out of their control, like being born with a genetic disease or becoming sick from a virus or bacteria. However, there are some things we all can do to help reduce our risk of acquiring a chronic disease that could lead to end-stage organ failure or limit our chances to donate.
<br><br>Many of the actions we can take benefit multiple organs, and they are no different from what we hear all the time from our own doctors and other medical professionals, in self-help books, on television programs, from the Internet, and from our families and friends. They include:
<ul>
<li>Get a physical assessment of your health status (preventive screening) so you can make a plan to become as healthy as you can be</li>
<li>Talk to your doctor about </li>
<ul>
<li>Your weight and your body mass index (BMI) and what this means for your health</li>
<li>Your blood pressure and what constitutes a normal range for your age</li>
<li>Your cholesterol level and what this means for your health</li>
<li>Your family history of disease and causes of death for your close relatives, then make plans accordingly to address any of these concerns</li>
</ul>
<li>Be physically active </li>
<ul>
<li>Engage in moderate to vigorous physical activity daily or at least 3 days per week. Even moderate exercise, such as walking, riding a bike, dancing, and swimming can make your heart stronger, help to keep your weight in check, reduce high blood pressure, and reduce stress</li>
</ul>
<li> Eat a lot of high-fiber foods including whole grains and at least five fruits and vegetables a day</li>
<li>If you have stress in your life, look for ways to reduce it</li>
<li>Do not smoke. If you don't smoke, don't start. If you smoke, try to quit and don't quit quitting</li>
<li>Don't abuse drugs and alcohol</li>
<li>Get enough sleep</li>
</ul>
